import React from "react";
import Head from "next/head";
import AddProdukLayout from "../../layout/addprodukForm";
import Top from "../../components/top";
import useResize from "../../hooks/useResize";
import { GetToken } from "../../utils/getToken";
import axios from "axios";
import { useRouter } from "next/router";

export async function getServerSideProps(context) {
  const API = process.env.NEXT_PUBLIC_API_ENDPOINT;

  let user = null;
  let allcookie = context.req.headers.cookie || "   ";
  let token = GetToken(allcookie);

  const res_categories = await axios.get(`${API}/categories`)
  const categories = res_categories.data.data;

  try {
    const res_user = await axios({
      method: `get`,
      url: `${API}/users`,
      headers: {
        Authorization: `Bearer ${token}`,
      },
    });
    user = res_user.data.data;
  } catch (error) {
    console.log(error);
  }
  return {
    props: {
      token,
      user,
      categories
    },
  };
}

export default function AddProduk({ user, token, categories }) {
  const router = useRouter();
  const screen = useResize();
  return (
    <>
      <Head>
        <title>Add Produk</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      {screen.md ? (
        <Top />
      ) : (
        <div className="d-flex d-row gap-2 m-3 fw-bold fs-4 justify-content-center">
          <i
            onClick={() => router.back()}
            className="bi bi-arrow-left pe-3"
            style={{ cursor: "pointer" }}
          ></i>
          <p className="">Lengkapi Detail Produk</p>
        </div>
      )}

      {screen.md ? (
        <div className="col-6 offset-3 mt-3 d-flex flex-column justify-content-center">
          <i
            onClick={() => router.back()}
            className="bi bi-arrow-left fs-3 mt-2"
            style={{ cursor: "pointer" }}
          ></i>

          <AddProdukLayout user={user} token={token} categories={categories}/>
        </div>
      ) : (
        <div className="px-3">
          <AddProdukLayout user={user} token={token} categories={categories}/>
        </div>
      )}
    </>
  );
}

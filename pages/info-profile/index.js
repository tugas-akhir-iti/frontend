import axios from "axios";
import Head from "next/head";
import Top from "../../components/top";
import InfoProfileLayout from "../../layout/infoprofileLayout";
import { useRouter } from "next/router";
import useResize from "../../hooks/useResize";
import { GetToken } from "../../utils/getToken";

export async function getServerSideProps(context) {
  const API = process.env.NEXT_PUBLIC_API_ENDPOINT;

  let user = null;
  let bank = null;
  let allcookie = context.req.headers.cookie || "   ";
  let token = GetToken(allcookie);

  try {
    const res_bank = await axios({
      method: `get`,
      url: `${API}/users/all/bank`,
      headers: {
        Authorization: `Bearer ${token}`,
      },
    });
    bank = res_bank.data.data;

    const res_user = await axios({
      method: `get`,
      url: `${API}/users`,
      headers: {
        Authorization: `Bearer ${token}`,
      },
    });
    user = res_user.data.data;
  } catch (error) {
    console.log(error);
  }
  return {
    props: {
      token,
      user,
      bank
    },
  };
}

export default function InfoProfile({ user, token, bank }) {
  const router = useRouter();
  const screen = useResize();

  return (
    <>
      <Head>
        <title>Info Profile</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      {screen.md ? (
        <Top>
          {/* <p className="fs-4">Info Akun</p> */}
        </Top>
      ) : (
        <div className="d-flex d-row gap-2 m-3 fw-bold fs-4 justify-content-center">
          <i
            onClick={() => router.back()}
            style={{ cursor: "pointer" }}
            className="bi bi-arrow-left pe-3"
          ></i>
          <p className="">Info Akun</p>
        </div>
      )}

      {screen.md ? (
        <div className="col-4 offset-4 mt-3 d-flex flex-column justify-content-center">
          <i
            onClick={() => router.back()}
            style={{ cursor: "pointer" }}
            className="bi bi-arrow-left fs-3 pe-5"
          ></i>
          <InfoProfileLayout user={user} token={token} bank={bank}/>
        </div>
      ) : (
        <div className="px-3">
          <InfoProfileLayout user={user} token={token} bank={bank}/>
        </div>
      )}
    </>
  );
}
